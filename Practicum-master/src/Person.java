public class Person
{
    private String ID;
    private String firstname;
    private String lastname;
    private String title;
    private int YOB;

    public Person(String ID, String firstname, String lastname, String title, int YOB)
    {
        this.ID = ID;
        this.firstname = firstname;
        this.lastname = lastname;
        this.title = title;
        this.YOB = YOB;
    }


}
